"""
Escribe un programa en Python para encontrar el área de un triángulo.

El area se calcula de la siguiente manera:

Area = (base * altura) /2
"""

base = float(input('Ingrese la base del traingulo: '))
altura = float(input('Ingrese la altura del traingulo: '))

area = (base * altura) / 2
print(f'El area del triangulo es: {area}')
